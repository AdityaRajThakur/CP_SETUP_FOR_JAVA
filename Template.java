	// this function is depth first search
	public static void dfs(int v, boolean[] visited, List<List<Integer>> adjlist) {
		if (visited[v])
			return;
		visited[v] = true;
		// System.out.print(v + " ");
		for (Integer integer : adjlist.get(v)) {
			if (visited[integer] == false) {
				dfs(integer, visited, adjlist);
			}
		}
	}

